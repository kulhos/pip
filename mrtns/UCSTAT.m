file2rtn(FILE)	; public ; extract routine name from file name 
	;-----------------------------------------------------------------------
	; This function returns $$PARSE^%ZFUNC(FILE), and translates "_" to "%"
	;
	quit $TR($$PARSE^%ZFUNC(FILE,"NAME"),"_","%")
