	;-----------------------------------------------------------------------
	; Check if clsR can be assigned to clsL.
	; Both clsL and clsR shall be one of the basic M types:
	; "String", "Number", or "Boolean".
	;
	; ARGUMENTS:
	; . clsL = class of leftexpr				/MECH=VAL
